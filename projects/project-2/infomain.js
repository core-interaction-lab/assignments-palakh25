function nav1() {
    console.log("paragraph1 was clicked");

    var paragraph= document.getElementById("paragraph1");
    paragraph.style.opacity= "1";


    const target = document.getElementById('paragraph1');
const newInnerHTML = target.innerHTML
  .split(/<br>\s*/i)
  .map((text, i) => '<p style="animation-delay:' + (400 * i) + 'ms">' + text + '</p>')
  .join(''); 

target.innerHTML = newInnerHTML;
}

